library(data.table)

# ========================================================
# LOAD DATA
# ========================================================

food = fread('http://www.principlesofeconometrics.com/poe4/data/dat/food.dat',header=F)

# ========================================================
# LOAD Summary
# ========================================================

head(food)
summary(food)

# ========================================================
# Regression  Y = a + bX + e
# ========================================================

reg1 <- lm(food)

# ========================================================
# Results
# ========================================================

reg1
summary(reg1)

# ========================================================
# SUMMARIES
# ========================================================

anova(reg1)
resid(reg1)
hist(residuals(reg1))

# ========================================================
# Save Results
# ========================================================

sink('newR.out')

